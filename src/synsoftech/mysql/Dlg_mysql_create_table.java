    private void ok() {
        String table_name = jTextField1.getText();
        int no = FitIn.toInt(jTextField2.getText());
        if (no <= 0 || table_name.isEmpty()) {
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(table_name, no));
        }
    }
