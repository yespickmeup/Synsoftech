    public void run() {

        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                String serverAddress = "192.168.1.159";
                Socket socket;
                try {
                    socket = new Socket(serverAddress, 1000);
                    in = new BufferedReader(new InputStreamReader(socket.getInputStream()));
                    out = new PrintWriter(socket.getOutputStream(), true);

                    while (true) {
                        String line = in.readLine();
                        if (line.startsWith("SUBMITNAME")) {
                            out.println(jTextField2.getText());

                        } else if (line.startsWith("NAMEACCEPTED")) {
                            System.out.println("Name Accepted!");
                            jButton1.setEnabled(false);
                            jTextField1.setEnabled(true);
                            jTextField2.setText("");
                        } else if (line.startsWith("MESSAGE")) {
                            jTextArea1.append(line.substring(8) + "\n");
                        }
                    }
                } catch (IOException ex) {
                    Logger.getLogger(Dlg_test_chat.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        });
        t.start();

    }
